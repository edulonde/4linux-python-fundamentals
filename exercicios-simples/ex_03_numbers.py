# 3) Escreva um script em Python que receba dois números e que seja realizado as seguintes operações:
# • soma dos dois números
# • diferença dos dois números
# O resultado deverá ser apresentado conforme a seguir - no exemplo foram digitados os números 4 e 2:

a = 4
b = 2


def soma():
    soma = a + b
    print(soma)


def subtracao():
    diferenca = a - b
    print(diferenca)


soma()
subtracao()
